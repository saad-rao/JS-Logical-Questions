                               // Questions on JavaScript Template Literals
// Q1: String Interpolation
// Create a template literal that dynamically inserts a user's name and age into a sentence.

let name = "Saad";
let age = 20;
let message = `Hello, my name is ${name} and I am ${age} years old`;
console.log(message);


// Q2: Expression Evaluation in Template Literals
// Calculate the area of a rectangle dynamically using template literals.

let length = 10;
let width = 5;
let areaMessage = `The area of the rectangle is ${length*width} square units`
console.log(areaMessage);


